let currentLanguage = 'en';

function initLanguageSelector() {
    const languageButton = document.getElementById('language-button');
    const languageDropdown = document.getElementById('language-dropdown');
    const languageOptions = document.querySelectorAll('.language__option');
    
    if (!languageButton || !languageDropdown) return;
    
    // Toggle dropdown
    languageButton.addEventListener('click', function(e) {
        e.stopPropagation();
        languageDropdown.classList.toggle('show');
        languageButton.classList.toggle('active');
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', function() {
        languageDropdown.classList.remove('show');
        languageButton.classList.remove('active');
    });
    
    // Language selection
    languageOptions.forEach(option => {
        option.addEventListener('click', function(e) {
            e.stopPropagation();
            const selectedLang = this.getAttribute('data-lang');
            changeLanguage(selectedLang);
            
            // Update active state
            languageOptions.forEach(opt => opt.classList.remove('active'));
            this.classList.add('active');
            
            // Close dropdown
            languageDropdown.classList.remove('show');
            languageButton.classList.remove('active');
        });
    });
}

function changeLanguage(lang) {
    currentLanguage = lang;
    
    // Update current language display
    const currentLangDisplay = document.getElementById('current-language');
    const langCodes = { 'en': 'EN', 'te': 'తె', 'kn': 'ಕ' };
    if (currentLangDisplay) {
        currentLangDisplay.textContent = langCodes[lang];
    }
    
    // Save language preference
    localStorage.setItem('selectedLanguage', lang);
    
    // Translate all elements
    translatePage(lang);
}

function translatePage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('placeholder', translations[lang][key]);
        }
    });
}

// Initialize language selector on page load
document.addEventListener('DOMContentLoaded', function() {
    initLanguageSelector();
    
    // Load saved language preference
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en';
    if (savedLanguage !== 'en') {
        changeLanguage(savedLanguage);
        
        // Set active state for saved language
        const savedOption = document.querySelector(`[data-lang="${savedLanguage}"]`);
        if (savedOption) {
            document.querySelectorAll('.language__option').forEach(opt => opt.classList.remove('active'));
            savedOption.classList.add('active');
        }
    }
});

